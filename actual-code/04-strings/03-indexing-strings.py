long_string = "This is a really long string. It goes on and on and on and on ....."

print(long_string[0:1:2])  # indexing!
print(long_string[10])  # First number is where to start!
print(long_string[1:10])  # Where to end -inclusive:exclusive
print(long_string[10:-2:2]) # in jumps of what?

print(long_string[::-1])

# substrings or indexing strings

print(len(long_string)) # length