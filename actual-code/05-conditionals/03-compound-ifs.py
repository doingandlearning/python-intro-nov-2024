favorite_genre = input("What's your favorite movie genre? ").lower()

if favorite_genre == "comedy":
    print("Let's watch a funny movie")
elif favorite_genre == "action":
    print("Let's watch an action movie.")
elif favorite_genre == "romcom":
    print("How about a rom-com?")
elif favorite_genre == "horror":
    print("Let's watch a scary movie.")
else:
    print("Can you suggest a movie then?")


