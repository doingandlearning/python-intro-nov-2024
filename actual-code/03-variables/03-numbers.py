count = 1

print(count)
print(type(count)) # int -> whole number (integer)

temperature = 12.7

print(temperature)
print(type(temperature)) # float -> decimal (floating point number)

print(1+2)
print(1 + 2.3)
print(1 - 2.3) # -1.3 -> floating point error
print(((1 * 10) - (2.3 * 10))/10)
print(3/2)  # decimal division (true division)
print(3//2)  # floor division (throws away the remainder)
print(3 ** 2) # 3 to the power of 2 (3 squared) (3^2)
print(12 % 5)  # modulo operator - clock arithmetic
print(15 % 2)
print(16 % 2)
print((12 + 3 + 5 + 2 + 6) % 12)
