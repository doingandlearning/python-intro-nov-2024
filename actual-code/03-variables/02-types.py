my_variable = "Kevin" # dynamically typed

print(my_variable) # prints out the value
print(type(my_variable))  # str -> string (words, letters, numbers, anything you can type)

my_variable = 123

print(my_variable)
print(type(my_variable)) # int -> integer -> whole number

my_variable = True

print(my_variable)
print(type(my_variable)) # bool -> boolean -> binary operation
print(id(my_variable))